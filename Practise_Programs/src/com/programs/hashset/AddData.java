package com.programs.hashset;

import java.util.HashSet;

public class AddData {

	public static void main(String[] args) {
		//Create a new Has Set
		HashSet<String> set=new HashSet<String>();
		set.add("Red");
		set.add("green");
		set.add("orange");
		set.add("Brown");
		set.add("white");
		
		//Print an HashSet
		System.out.println("HashSet is :"+set);
		

	}

}
